package com;


public class VehicleDetails {
    private  String registration_number = null;
    private  String vehicle_make = null;
    private  String registration_date = null;
    private  String manufacturer_year = null;

    public String getRegistration_number() {
        return registration_number;
    }

    public void setRegistration_number(String registration_number) {
        this.registration_number = registration_number;
    }

    public String getVehicle_make() {
        return vehicle_make;
    }

    public void setVehicle_make(String vehicle_make) {
        this.vehicle_make = vehicle_make;
    }

    public String getRegistration_date() {
        return registration_date;
    }

    public void setRegistration_date(String registration_date) {
        this.registration_date = registration_date;
    }

    public String getManufacturer_year() {
        return manufacturer_year;
    }

    public void setManufacturer_year(String manufacturer_year) {
        this.manufacturer_year = manufacturer_year;
    }
}

